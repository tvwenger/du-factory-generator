export enum ElementType {
    CONTAINER = "container",
    OTHER = "other",
}

/**
 * Properties common to each item type
 */
export interface CommonProperties {
    readonly name: string
    readonly tier: Tier
    readonly category: Category
    readonly volume: Liter
    readonly transferBatchSize: Quantity
    readonly transferTime: Minutes
}

/**
 * Item types
 */
export interface Ore extends CommonProperties {
    readonly type: ItemType.ORE
}

export interface Gas extends CommonProperties {
    readonly type: ItemType.GAS
}

export interface Catalyst extends CommonProperties {
    readonly type: ItemType.CATALYST
}

export interface Product extends CommonProperties {
    readonly type: ItemType.PRODUCT
}

export interface ContainerElement extends CommonProperties {
    readonly type: ItemType.ELEMENT
    readonly elementType: ElementType.CONTAINER
    readonly capacity: Liter
}

export interface OtherElement extends CommonProperties {
    readonly type: ItemType.ELEMENT
    readonly elementType: ElementType.OTHER
}

/**
 * Ore type guard
 * @param item Item to check
 */
export function isOre(item: Item): item is Ore {
    return item.type === ItemType.ORE
}

/**
 * Gas type guard
 * @param item Item to check
 */
export function isGas(item: Item): item is Gas {
    return item.type === ItemType.GAS
}

/**
 * Craftable type guard
 * @param item Item to check
 */
export function isCraftable(item: Item): item is Craftable {
    return !isOre(item)
}

/**
 * ContainerElement type guard
 * @param item Item to check
 */
export function isContainerElement(item: Item): item is ContainerElement {
    return item.type === ItemType.ELEMENT && item.elementType === ElementType.CONTAINER
}

/**
 * Catalyst type guard
 * @param item Item to check
 */
export function isCatalyst(item: Item): item is Catalyst {
    return item.type === ItemType.CATALYST
}

/**
 * Returns a new Ore type
 * @param name Ore name
 * @param tier Ore tier
 */
export function ore(
    name: string,
    tier: Tier,
    transferBatchSize: Quantity,
    transferTime: Minutes,
): Ore {
    return {
        type: ItemType.ORE,
        name,
        tier,
        category: Category.ORE,
        volume: 1,
        transferBatchSize,
        transferTime,
    }
}

/**
 * Returns a new Gas type
 * @param name Ore name
 */
export function gas(name: string, transferBatchSize: Quantity, transferTime: Minutes): Gas {
    return {
        type: ItemType.GAS,
        name,
        tier: Tier.BASIC,
        category: Category.GAS,
        volume: 1,
        transferBatchSize,
        transferTime,
    }
}

/**
 * Returns a new Catalyst type
 * @param name Catalyst name
 * @param tier Catalyst tier
 * @param volume Catalyst volume
 */
export function catalyst(
    name: string,
    tier: Tier,
    volume: Liter,
    transferBatchSize: Quantity,
    transferTime: Minutes,
): Catalyst {
    return {
        type: ItemType.CATALYST,
        name,
        tier,
        category: Category.CATALYST,
        volume,
        transferBatchSize,
        transferTime,
    }
}

/**
 * Returns a new Product type
 * @param name Product name
 * @param tier Product tier
 * @param category Product category
 * @param volume Product volume
 */
export function product(
    name: string,
    tier: Tier,
    category: Category,
    volume: Liter,
    transferBatchSize: Quantity,
    transferTime: Minutes,
): Product {
    return {
        type: ItemType.PRODUCT,
        name,
        tier,
        category,
        volume,
        transferBatchSize,
        transferTime,
    }
}

/**
 * Returns a new OtherElement type
 * @param name Element name
 * @param category Element category
 * @param volume Element volume
 */
export function otherElement(
    name: string,
    category: Category,
    volume: Liter,
    transferBatchSize: Quantity,
    transferTime: Minutes,
): OtherElement {
    return {
        type: ItemType.ELEMENT,
        elementType: ElementType.OTHER,
        name,
        tier: Tier.ELEMENT,
        category,
        volume,
        transferBatchSize,
        transferTime,
    }
}

/**
 * Returns a new ContainerElement type
 * @param name Element name
 * @param category Element category
 * @param volume Element volume
 * @param capacity Container capacity
 */
export function containerElement(
    name: string,
    category: Category,
    volume: Liter,
    capacity: Liter,
    transferBatchSize: Quantity,
    transferTime: Minutes,
): ContainerElement {
    return {
        type: ItemType.ELEMENT,
        elementType: ElementType.CONTAINER,
        name,
        tier: Tier.ELEMENT,
        category,
        volume,
        capacity,
        transferBatchSize,
        transferTime,
    }
}

