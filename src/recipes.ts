import { Craftable, Item, ITEMS, Minutes, OtherElement, Quantity } from "./items"

/**
 * Batch holds a product and a quantity
 */
export interface Batch {
    readonly item: Item
    readonly quantity: Quantity
}

/**
 * Recipe holds all relevant recipe information
 */
export interface Recipe {
    readonly product: Batch
    readonly time: Minutes
    readonly industry: OtherElement
    readonly byproducts: Batch[]
    readonly ingredients: Batch[]
}

/**
 * Return a new batch type
 * @param item Batch item
 * @param quantity Production batch size
 */
export function batch(item: Item, quantity: Quantity): Batch {
    return {
        item,
        quantity,
    }
}

/**
 * Get an item's recipe
 * @param item The item for which to get a recipe
 */
export function findRecipe(item: Craftable): Recipe {
    const recipe = RECIPES.find((process) => process.product.item === item)
    if (recipe) {
        return recipe
    } else {
        throw new Error(`No recipe found for product "${item.name}".`)
    }
}

